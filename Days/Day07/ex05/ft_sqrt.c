int	ft_sqrt(int nb)
{
	int	i;
	int	res;

	i = 0;
	res = 0;
	while (res <= nb && i < 46341)
	{
		res = i * i;
		if (res == nb)
			return (i);
		i++;
	}
	return (0);
}
